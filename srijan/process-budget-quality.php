              <h1>Meeting Budgets Without Compromising Quality</h1>
              <header class="sub-header">
                <?php echo file_get_contents("img/team.svg"); ?>
              </header>
              <h2>Right Products are Built with the Right Set of Features</h2>
              <p>Right products are built when you change your product development perspective from:

                "These are the important features for my customers."

                to

                "What do I not want to release to my customers early on?"</p>

                <p>This is not to say that you do not release more features to your clients, but this thinking forces you to focus most on what you need to get done as part of your Agile development services.</p>

                <p>Another way to force yourself to prioritise the most important business features is by keeping a tab on prices. If you knew that you can add/remove all the features that you want, but the development team is available to you only for, say 10 weeks—which are the most important features that you would roll out?</p>

                <p>Giving flexibility to features enables staying within budgets, more times than you would imagine.</p>


              </section>


