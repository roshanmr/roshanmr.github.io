          <h2>Transitioning to Agile</h2>

          <p>Breaking set patterns and adopting a new way of work can seem difficult at first. But onboarding your team to agile and scrum can actually be a fun exercise.</p>

          <p>Stefan Wolpers has a great post where he shares how he transitioned his start-up team to agile and formed scrum team #1. What’s best about this post is that it tells you how to approach the transition process. Starting from why the transition, to identifying current patterns and dysfunctions, to finally forming the first scrum team.</p>

          <p>Stefan goes into greater detail, laying down the exact steps to getting your first scrum team. And while the post outlines what he did for a fast growing start-up, the steps wouldn’t be different when you are trying to form the first scrum team at your enterprise.</p>

          <h2>Evolving Project Managers</h2>

          <p>Most enterprises have a team of project managers who make sure that all projects are on track. From addressing roadblocks to ensuring delivery, they see to it that the team is working as well as it should. But with agile teams, things work a little differently and so the role of the project manager changes as well.</p>

          <p>Mike Cohn adeptly addresses this in his post “The Roles of a Project Management Office in Scrum”. Adopting scrum does away with a lot of a project manager’s conventional responsibilities; assigning them to the ScrumMaster, product owner and the team. But Mike explains that project managers still have a very critical role to play here, especially at large enterprises.</p>

          <p>Since enterprises will have a few different projects running simultaneously, there will be various scrum teams in place. Project managers now have the role of facilitating training and ensuring consistency across all teams. Mike’s post is a comprehensive list of all the new responsibilities that project managers now have, to make sure that scrum teams can function effectively.</p>

          <h2>Managing Scrum Team Challenges</h2>

          <p>As an enterprise, if you have just started working with the agile scrum methodology, challenges are bound to crop up. It is a new way of work, with new tools and requirements, and there will be a lot of questions and unforeseen problems.</p>

          <p>Luis Goncalves shares a great way to address these challenges in his post “How to Manage Organisational Scrum Impediments?” He shares a framework that enables organizations to continuously learn and improve with each project. He shares a tool termed as the ‘Organisational Improvement Board’ and outlines how to use it. Luis shares every step of effectively using this tool, in great detail.</p>
          
          <h2>Pivoting at the Right Time</h2>

          <p>Enterprises usually go large when they start off with a product, investing huge amounts of time and money. There is obviously a lot at stake and building the wrong product can be disastrous. But when you are so involved in building the various parts of a product, it is possible for the team to lose sight of the big picture.</p>

