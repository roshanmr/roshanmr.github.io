              <h2>Role of a Scrum Master</h2>
              <p>
                The scrum master acts as a coach, guiding the team to ever-higher levels of cohesiveness, self-organization, and performance. While a team’s deliverable is the product, a scrum master’s deliverable is a high-performing, self-organizing team.
              </p>

              <p>The scrum master is the team’s good shepherd, its champion, guardian, facilitator, and scrum expert.  The scrum master helps the team learn and apply scrum and related agile practices to the team's best advantage. The scrum master is constantly available to the team to help them remove any impediments or road-blocks that are keeping them from doing their work. The scrum master is not—we repeat, not—the team’s boss. This is a peer position on the team, set apart by knowledge and responsibilities not rank.</p>

              <h3>The Scrum-master's role in a nutshell:</h3>

              <ul>
                <li>Scrum expert and advisor</li>
                <li>Coach</li>
                <li>Impediment Bulldozer</li>
                <li>Facilitator</li>
              </ul>
              <p><a href="#" class="source-link">Source <i class="fa fa-external-link"></i></a></p>
              

              <h2>Role of the Team</h2>
              <p>High-performing scrum teams are highly collaborative; they are also self-organizing. The team members doing the work have total authority over how the work gets done. The team alone decides which tools and techniques to use, and which team members will work on which tasks. The theory is that the people who do the work are the highest authorities on how best to do it. Similarly, if the business needs schedule estimates, it is the team members who should create these estimates.</p>

              <p>A scrum team should possess all of the skills required to create a potentially shippable product. Most often, this means we will have a team of specialists, each with their own skills to contribute to the team's success.  However, on a scrum team, each team member's role is not to simply contribute in their special area. The role of each and every team member is to help the team deliver potentially shippable product in each sprint. Often, the best way for a team member to do this is by contributing work in their area of specialty. Other times, however, the team will need them to work outside their area of specialty in order to best move backlog items (aka user stories) from "in progress" to "done." What we are describing is a mindset change from "doing my job" to "doing the job." It is also a change in focus from "what we are doing" (work) to what is getting done (results).</p>

              <h3>The team member role in a nutshell:</h3>
              <ul>
              <li>is responsible for completing user stories to incrementally increase the value of the product</li>
                <li>self-organizes to get all of the necessary work done</li>
                <li>creates and owns the estimates</li>
                <li>owns the “ how to do the work” decisions</li>
                <li>avoids siloed “not my job” thinking</li>
              </ul>
