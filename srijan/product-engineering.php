              <p>Srijan engages with its clients in Discovery Workshops, brainstorming together, exchanging ideas, documenting usage scenarios and creating white-board mockups</p>

                <p>We translate these ideas into working HTML/CSS prototypes using technologies such as Angular.js.These 6-8 weeks invested in prototyping your product might help you build the right product, which your clients will buy even before it is built.</p>

                <h2>Agile Processes</h2>

                <p>Srijan started adopting the agile methodology in 2011 and now has all products being developed using this methodology. Under the continuous and rigorous coaching by our agile coach, Avienaash Shiralige, we are well on our way to mature as an agile company. We have well-designed and deeply entrenched agile processes to ensure the success of all products. Our focus is to help our clients develop the right product while trying to keep costs low using a  dedicated, lean and cross-functional team. One of the key traits of an agile team for product development is the ability to ask the right questions so that the product shapes up with a laser-sharp focus on the end user. Srijan is committed to developing such empowered teams which are critical to product success.</p>

                <p>The key idea that governs product development at Srijan is transparency. Every client sees this in our project estimates, the way we set up the team, in our weekly burn-rate model, and our discussions around fixed-cost models.</p>

                <h2>Quality &amp; Testing Processes</h2>
                <p>Srijan’s quality assurance processes work in three broad steps. One, we ensure 100 per cent mapping of user stories to test cases. Two, we have a review process that ensures all test scenarios are covered. Three, we generate reports for all types of testing so you can view how the tests are being done, and their results. This enables the product team to ensure that the product is as per the defined quality standards.</p>

