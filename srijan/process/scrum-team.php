              <h2>Role of the Product Owner</h2>
              <p>A development team represents a significant investment on the part of the business. There are salaries to pay, offices to rent, computers and software to buy and maintain, and on and on. The product owner is responsible for maximizing the return the business gets on this investment (ROI).</p>

              <p>
                One way that the product owner maximizes ROI is by directing the team toward the most valuable work, and away from less valuable work. That is, the product owner controls the order, sometimes called priority, of items in the team’s backlog. In Scrum, no one but the product owner is authorized to ask the team to do work or to change the order of backlog items.
              </p>

              <p>
                Another way that the product owner maximizes the value realized from the team's efforts is to make sure the team fully understands the requirements. If the team fully understands the requirements, then they will build the right thing, and not waste time building the wrong thing. The product owner is responsible for recording the requirements, often in the form of user stories (eg, “As a &lt;role&gt;, I want &lt;a feature&gt;, so that I can &lt;accomplish something&gt;”) and adding them to the product backlog. Each of these users stories, when completed, will incrementally increase the value of the product.  For this reason, we often say that each time a user story is done we have a new product increment.
              </p>

              <h3>The Product Owner role in a nutshell:</h3>

              <ul>
                <li>holds the vision for the product represents the interests of the  business</li>
                <li>represents the customers</li>
                <li>owns the product backlog</li>
                <li>orders (prioritizes) the items in the product backlog</li>
                <li>creates acceptance criteria for the backlog items</li>
                <li>is available to answer team members’ questions</li>
              </ul>
